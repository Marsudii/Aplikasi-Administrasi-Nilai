    private void prodiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_prodiItemStateChanged
        String jurusan = prodi.getSelectedItem().toString();
        if(jurusan == "Teknik Informatika"){
            b_bulanan = 300000;
            biaya.setText("Rp. "+b_bulanan);
        }else if(jurusan == "Ekonomi"){
            b_bulanan = 200000;
            biaya.setText("Rp. "+b_bulanan);
        }else if(jurusan == "MIPA"){
            b_bulanan = 225000;
            biaya.setText("Rp. "+b_bulanan);
        }
    }//GEN-LAST:event_prodiItemStateChanged

