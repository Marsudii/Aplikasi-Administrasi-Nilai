     private void Grade() {
        kehadiran = Integer.parseInt(hadirTextField.getText())*10/100;
        tugas = Integer.parseInt(tugasTextField.getText())*20/100;
        uts = Integer.parseInt(utsTextField.getText())*30/100;
        uas = Integer.parseInt(uasTextField.getText())*40/100;
        total_nilai = kehadiran+tugas+uts+uas;
        akhirTextField.setText(""+total_nilai);
        
        if(total_nilai >= 80 && total_nilai <= 100){
            grade = "A";
            gradeTextField.setText(grade);
        }else if(total_nilai >= 70 && total_nilai <= 80){
            grade = "B";
            gradeTextField.setText(grade);
        }else if(total_nilai >= 60 && total_nilai <= 70){
            grade= "C";
            gradeTextField.setText(grade);
        }else if(total_nilai >= 50 && total_nilai <= 60){
            grade = "D";
            gradeTextField.setText(grade);
        }else{
            grade = "E";
            gradeTextField.setText(grade);
        }
    }

